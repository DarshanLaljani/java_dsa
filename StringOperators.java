public class StringOperators {
    public static void main(String[] args) {
        System.out.println('a'+'b');
        System.out.println("a"+"b");
        System.out.println((char)('a'+3));
        System.out.println("a" +1);

    }
    // Outputs for the above operations
//195
//ab
//d
//a1
}
